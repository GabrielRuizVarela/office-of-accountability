export type FactcheckStatus =
  | 'confirmed'
  | 'alleged'
  | 'denied'
  | 'under_investigation'

export interface FactcheckItem {
  id: string
  claim_en: string
  claim_es: string
  status: FactcheckStatus
  source: string
  source_url: string
  detail_en?: string
  detail_es?: string
}

export type InvestigationCategory =
  | 'political'
  | 'financial'
  | 'legal'
  | 'media'
  | 'coverup'

export interface InvestigationTimelineEvent {
  id: string
  date: string
  title_en: string
  title_es: string
  description_en: string
  description_es: string
  category: InvestigationCategory
  sources: string[]
}

export interface Actor {
  id: string
  name: string
  role_en: string
  role_es: string
  description_en: string
  description_es: string
  nationality: string
  status_en?: string
  status_es?: string
}

export interface MoneyFlow {
  id: string
  from_label: string
  to_label: string
  amount_usd: number
  date: string
  source: string
}

export type VerificationStatus =
  | 'verified'
  | 'partially_verified'
  | 'unverified'

export interface EvidenceDoc {
  id: string
  title: string
  type_en: string
  type_es: string
  date: string
  summary_en: string
  summary_es: string
  source_url: string
  verification_status: VerificationStatus
}

export interface ImpactStat {
  value: string
  label_en: string
  label_es: string
  source: string
}

export interface GovernmentResponse {
  id: string
  date: string
  action_en: string
  action_es: string
  effect_en: string
  effect_es: string
  source_url?: string
}

